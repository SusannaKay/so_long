#include "ft_printf.h"

int	ft_put_uns(unsigned int n)
{
	char	c;
	int		count;

	count = 0;
	if (n >= 10)
		count += ft_put_uns(n / 10);
	c = n % 10 + '0';
	count += write(1, &c, 1);
	return (count);
}
